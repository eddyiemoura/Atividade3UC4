    public void mostraTela(TelaInicio obj, int pos) {
        this.telaInicio = obj;
        this.posPaciente = pos;
        this.paciente = ListaPacientes.getPaciente(pos);

        carregaFormulario();
        setVisible(true);

    }

    private void carregaFormulario() {
        consReal.setSelected(false);
        if (this.paciente.getConsultaR().equals("Sim")) {
            consReal.setSelected(true);
            consReal.setEnabled(false);
            btnFinalizar.setEnabled(false);

        }
        consultaObservacoes.setText(this.paciente.getObservacoes());

    }

