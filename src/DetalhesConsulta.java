    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        this.paciente.setObservacoes(consultaObservacoes.getText());

        if (consultaObservacoes.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "É necessário preencher o campo de observações.");
        } else {
            if (consReal.isSelected()) {
                this.paciente.setConsultaR("Sim");
            } else {
                this.paciente.setConsultaR("Não");
            }

            ListaPacientes.AtualizarRegistro(paciente, posPaciente);

            telaInicio.atualizarTabela();
            
            JOptionPane.showMessageDialog(null, "Consulta atualizada.");

            dispose();
        }

    }//GEN-LAST:event_btnFinalizarActionPerformed

